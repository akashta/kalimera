def apply_fixes(input_file, output_file):
    rows = []
    with open(input_file, 'r', encoding='utf-8') as f:
        reader = csv.reader(f)
        for row in reader:
            rows.append(row)

    field_map = {'greek': 1, 'english': 2, 'russian': 3}
    fixed_count = 0

    for i, row in enumerate(rows):
        if i == 0:  # header
            continue
        if len(row) < 4:
            continue

        greek_word = row[1]
        if greek_word in FIXES_BY_WORD:
            fixes = FIXES_BY_WORD[greek_word]
            for field, new_value in fixes.items():
                col_idx = field_map[field]
                if col_idx < len(row):
                    old_value = row[col_idx]
                    if old_value != new_value:
                        row[col_idx] = new_value
                        print(f"Fixed {greek_word} [{field}]: {repr(old_value)[:40]} -> {repr(new_value)[:40]}")
                        fixed_count += 1

    with open(output_file, 'w', encoding='utf-8', newline='') as f:
        writer = csv.writer(f)
        writer.writerows(rows)

    print(f"\nTotal fixes applied: {fixed_count}")
    print(f"Output written to: {output_file}")


